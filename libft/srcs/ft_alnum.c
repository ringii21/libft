#include "libft.h"

int ft_isalnum(int val)
{
	if ((val >= -2147483648 && val <= 2147483647) || (val >= 'a' && val <= 'z')
		|| (val >= 'A' && val <= 'Z'))
		return (1);
	return (0);
}
